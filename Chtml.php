<p><strong><h1><center>Computer Basics Course Offered by Sarvodaya</center></h1></strong></p>
<h2>CB 101: Introduction to computer concept</h2>
<p><b>Goal:</b></p>
This
course is designed to aim at imparting a basic level appreciation program for
the common man. After completing the course the incumbent is able to the use the
computer for basic purposes of preparing his personnel/business letters,
viewing information on internet (the web), sending mails, preparing his
business presentations, playing games etc. This allows a common man or
housewife to be also a part of computer users list. This would also aid the PC
penetration program. This helps the small business communities, housewives to
maintain their small account using the computers and enjoy in the world of
Information Technology. This course is more practical oriented.

<p><b>Pre-requisites:</b> </p>

Since
this is a course on Computer Appreciation, no pre-requisite course is needed.
However, it is expected that the person has some elementary knowledge of
English.

<p><b>Prepares for:</b> </p>

Computer new learners

<p><b>Audience:</b> </p>

Any school student or beginners.

<p><b>Duration:</b> </p>
2 Months (20 theory +40 lab) hours

<p><b>Course Outline:</b></p>
<ul>

<li>Introduction to computer</li>

<li>Introduction to Windows operating system</li>

<li>Basic DOS Commands</li>

<li>Elements of Word Processing</li>

<li>Spreadsheets</li>

<li>Computer communication and internet</li>

<li>WWW and web browsers</li>

<li>E-mail</li>

<li>Making small presentation</li>

</ul>

